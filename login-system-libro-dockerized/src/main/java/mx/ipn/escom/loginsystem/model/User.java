package mx.ipn.escom.loginsystem.model;

import java.util.List;
import java.util.ArrayList;

import mx.ipn.escom.loginsystem.model.FavoriteBook;
import jakarta.persistence.*; // Cambia javax.persistence por jakarta.persistence

@Entity
public class User {
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private Long id;

    private String username;
    private String password;
    private String role;

    @OneToMany(mappedBy = "user", cascade = CascadeType.ALL, orphanRemoval = true)
    private List<FavoriteBook> favorites = new ArrayList<>();

    @Column(name = "profile_image")
    private String profileImage; // aqu√≠ guardaremos la ruta o nombre del archivo


    // Getters y Setters
    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getRole() {
        return role;
    }

    public void setRole(String role) {
        this.role = role;
    }

    public String getProfileImage() {
        return profileImage;
    }

    public void setProfileImage(String profileImage) {
        this.profileImage = profileImage;
    }
}